<p class="bt5"> The applicants should read the available instructions
carefully while filling in the online Application Form. The following major
points are to be noted : &nbsp; </p> <ul style="margin-bottom: 5px;">
<li><strong> Application Fee</strong> : The payment of the Application fee is
to be made online using credit card/debit card/net-banking. The transaction
details may be printed and preserved for later reference. <strong>The
submission of the Application Form will remain incomplete until the required
Application Fee is transferred successfully.<br> </strong></li> <li><strong>
Multiple Programmes</strong> : Candidates applying for multiple Programmes must
apply separately with separate Application Fee for each Programme. However,
there will be one entrance test per department for Integrated dual B.Sc. &amp;
M.Sc. and Integrated B.Sc. B.Ed. programmes. Similarly, there will be one
entrance test for M.Tech (IT) and M. Tech (CSE) programmes. </li> <li> Students
applying for integrated programmes for the same department can apply in single
application form. The candidates can give their preference either for a single
programme or both the programmes. The one who applies for both the programmes
will be considered as applying for two programmes and will have to pay two
times the application fee. However for single examination only one time fee
will be applicable.  </li> <li><strong> B. Tech. Programmes</strong> : In the
case of B. Tech. programmes, a candidate needs to apply in a single application
form only. The selected candidates will opt for the Programme/ Department based
on the availability of seats at the time of their turns during the admission
process. <strong><br> </strong></li> <li><strong> Candidates yet to obtain the
last qualifying Degree/Diploma/Certificate</strong> : Candidates who have
already finished their qualifying examinations or expect to finish all the
components, including practical, viva-voce (if any), and backlog courses/
papers of earlier semesters before the date of admission may also apply.<br>
</li> <li><strong> Admit Card for Entrance Examinations</strong> : Candidates
shortlisted for appearing University entrance examinations (not for B.Tech.
Programmes) will be intimated separately through e-mail/SMS on their registered
e-mail IDs/mobile numbers to download their admit cards.</li> <li></li>
<li><strong><br> DOCUMENTS  TO BE UPLOADED </strong>: </li> <li> Copy of the
JEE (Main) 2021 admit card, if applying for the B. Tech. Programmes. </li> <li>
Copy of the Permanent Residence Certificate (PRC) issued by the competent
authority of any North East State, if applying for the B.Tech. Programmes or
M.Sc. Programme in Molecular Biology and Biotechnology under the North East
quota. </li> <li> Copy of the valid GATE score card, if applying for direct
admission to any M.Tech. Programme. </li> <li> Copy of the relevant certificate
issued by the competent authority, if seeking admission under any reserved
category as mentioned. </li> <li> Copy of the Sponsorship/No Objection
Certificate issued by the employer, if the candidate is employed. </li> <li> A
passport size photograph</li> <li> Scanned copy of the signature</li> <li>
Relevant certificate as evidence of representing State or Country in sports, if
any (<strong>Not applicable for B. Tech, M. Tech and Ph.D programmes</strong>)
</li> <li>Copy of the valid CEED score card &amp; portfolio, if applying for
direct admission to <strong>M.Des. Programme</strong>.</li> </ul> <p
class="bt5"><br/><strong>STEPS TO COMPLETE THE ONLINE APPLICATION FORM
</strong><p> <p class="bt5">Step 1:  Register using the "Sign Up" process. The
email ID and mobile number used for registration must be valid and functional
and should belong to the applicant. Communications will be sent only to the
registered email address or mobile number. </p> <p class="bt5">Step 2:
Click "Apply Now" in your dashboard to proceed with the application form
fill-up process.        </p> <p class="bt5">Step 3: Enter data for the
Eligibility Criteria.        </p> <p class="bt5">Step 4: Upload documents from
Class X onwards under Addl. Documents Upload        </p> <p class="bt5">Step 5:
Pay the prescribed fee for online registration  using the online payment
gateway.        </p> <p class="bt5">Step 6: Take a printout of the information
submitted. This printout is to be taken along with the other original documents
at the time of admission.        </p> <p class="bt5">Step 7: Please keep the
following documents handy for online submission before filling up the
application form. Scan and store the following documents as separate files as
they will need to be uploaded as required during the ‘Online Application’
process.        </p> <p class="bt5" > <br/><strong> MANDATORY DOCUMENTS:
</strong> </p> <p>1. Copy of the JEE (Main) 2021 admit card, if applying for
the B. Tech. Programmes. <br> 2.       Copy of the Permanent Residence
Certificate (PRC) issued by the competent authority of any North East State, if
applying for the B.Tech. Programmes or M.Sc. Programme in Molecular Biology and
Biotechnology under the North East quota.<br> 3. Copy of the valid GATE score
card, if applying for direct admission to any M.Tech. Programme.<br> 4. Copy of
the relevant certificate issued by the competent authority, if seeking
admission under any reserved category as mentioned. <br> 5. Copy of the
Sponsorship/No Objection Certificate issued by the employer, if the candidate
is employed.<br> 6. Relevant certificate as evidence of representing State or
Country in sports, if any (<strong>Not applicable for B. Tech, M. Tech and Ph.D
programmes</strong>) <br> 7. Upload CEED Score Card & Portfolio document
(<strong>Applicable for M. Des. programme</strong>) <br> 8. Upload Undertaking
Document (<strong>Format available under News and Updates Section</strong>)
<br> 9. Passport size photo (PNG/JPEG/JPG) (height : 250 | Width : 200 Pixels)
(100 KB) <br> 10. Scanned Signature (PNG/JPEG/JPG) (height : 150 | Width : 200
Pixels) (100 KB) <br> <br> <strong>Please ensure that the above scanned
documents are self attested, legible and readable. Applications submitted with
illegible documents are liable to be rejected.    </strong>  </p>
