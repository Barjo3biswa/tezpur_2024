<p align="justify">Online applications are invited from eligible candidates for
admission to various programmes of study for the academic year 2021-22. Interested and eligible
candidates may APPLY ONLINE through theTezpur University admissions portal at <a
href="http://www.tezuadmissions.in">www.tezuadmissions.in</a> by paying a fee
of <b>&#8377;800</b>&nbsp;(<b>&#8377;400</b>&nbsp;for SC, ST
and PWD) candidates. For international candidates the fee is&nbsp;<b>$25</b>.
Additional bank charges may apply.&nbsp;<b><span style='color:blue'>Online
submission of application for M. Des is closed for the session 2021-22.</span></b></p>

<p align="justify">Applicants are advised to read the instructions carefully before filling in the online Application Form. The following major points are to be noted at the time of filling in the Application Form: </p>

<p align="left"><strong>- Application Fee</strong></p>

<p align="justify">The payment of the application fee is to be made ONLINE using credit card/debit card/net-banking options only. The transaction details may be printed and preserved for later reference.&nbsp;It is to be noted that <strong>the submission of the Application Form will remain treated as incomplete until the required Application Fee is transferred successfully.</strong></p>

<p align="left"><strong>1. Multiple Programmes:</strong></p>

<p align="justify">Candidate applying for multiple Programmes must apply separately with separate Application fee for each of the Programmes they apply for.</p>

<p align="justify">Candidates applying for integrated Programmes under the same Department can apply in single application form. Here, candidates can select their preferences either
for a single programme or for both the programmes.</p>

<p align="left">- However, candidates need to pay single application fee for Integrated M.Sc. and Integrated B.Sc. B.Ed. Programmes under the same Department. Similarly, there will be
single application fee for M.Tech (IT) and M. Tech (CSE) Programmes.</p>

<p align="left"><strong>2. B. Tech. Programmes:</strong></p>

<p align="left">In case of B. Tech. programmes, a candidate needs to apply in single application
form only. The selected candidates can opt for the preferred Branch of Engineering based on the availability of seats at the time of their turns during the admission process.</p>

<p align="left"><strong>3.Candidates yet to obtain the last qualifying Degree/Diploma/Certificate: </strong></p>

<p align="justify">Candidates who have already completed/appeared their qualifying Degree / Diploma/ Certificate examinations or expected to complete the qualifying examinations with all the components, including practical, viva-voce (if any), and courses of earlier semesters may also apply.</p>


<p align="left"><strong>Documents To Be Uploaded:</strong></p>

<p align="left">- Copy of the JEE (Main) 2021 admit card, if applying for the B. Tech. Programmes.<br>
- Copy of the Permanent Residence Certificate (PRC) issued by the competent authority of any North Eastern (NE) States, if applying for the B.Tech. Programmes or M.Sc. Programme in Molecular Biology and Biotechnology under the North East (NE) quota.<br>
- Copy of the valid GATE score card, if applying for direct admission to any M.Tech. Programmes.If applying for M.Sc. in MBBT, a copy of valid GAT-B (Graduate Aptitude Test - Biotechnology) score card.<br>
- Copy of the valid score card of GATE/NET/SET etc (if any), if applying for Ph. D.<br>
- Copy of the relevant category certificate issued by the competent authority, if seeking admission under any reserved category.<br>
- Copy of the Sponsorship/No Objection Certificate (NOC) issued by the employer if the candidate is employed.<br>
- Separate undertakings for category and appearing qualifying examination.<br>
- A passport size photograph.<br>
- Scanned copy of the signature.<br>
- A certificate issued by Defence authority that the candidate is the widow/ward/wife of the Defence personal as per the Priority set by the Govt of India.<br>
- Relevant certificate as evidence of representing State or Country in sports, if any.
</p>

<p align="center"><strong>STEPS TO COMPLETE THE ONLINE APPLICATION FORM</strong></p>

<p align="left"><strong>Step A:</strong> Register using the &quot;Sign Up&quot; process. The email ID and mobile number used for registration must be valid and functional and should belong to the applicant. Communications will be done only to the registered email address or mobile number.</p>

<p align="left"><strong>Step B:</strong> Click &quot;Apply Now&quot; in your dashboard to proceed with the application form fill-up process.</p>

<p align="left"><strong>Step  C:</strong> Select programme</p>

<p align="left">Note: When you are going to apply for two integrated programs under the same Department [for example: Integrated B.Sc. B.Ed. in Chemistry (8 semesters) and Integrated M.Sc. in Chemistry (with provision of lateral exit)], YOU HAVE TO SELECT THE PREFERENCE
Numbers as 1 and 2, depending upon your preference between the programmes. <strong>However,
in case of applying for only one integrated programme under the same Department</strong>,
you should select 1 as the preference number.</span></p>

<p align="left">Now Check eligibility and click next</p>

<p align="left"><strong>Step D:</strong></p>

<p align="left">Step 1 - Enter the Personal Information<br>
				Step 2 - Enter Address and Contact Information<br>
				Step 3 - Enter Academic Details</p>

<p align="left"><strong>- Integrated Programmes and B Tech:</strong> </p>

<p align="left"> Enter qualification details (up to Class XII) and fill in the subject wise marks as required in the respective field (wherever applicable). </p>

<p align="left"><strong>-PG Programme:  </strong></p>

<p align="left"> Enter qualification details (up to Degree level). Candidates have to enter semester wise marks by adding new row (Click ADD NEW button) for the qualifying / pre-qualifying examination or for result awaited.</p>

<p align="left"><strong>-PhD Programme: </strong></p>

<p align="left"> Enter qualification details (up to PG level). Candidates have to enter semester wise marks by adding new row (Click ADD NEW button) for the qualifying / pre-qualifying
examination or for result awaited.</p>

<p align="left">Step 4 - Upload the following (against Document1, Document2 etc.), accept declaration and submit</p>

<p align="left">a. Undertaking for Category/ Caste Certificate (for all programmes)<br>
				b. Undertaking for (Passed/Appeared) (for all programmes)<br>
				c. Passport Size Photo<br>
				d. Scanned copy of Signature<br>
				e. GATE Score card (If applicable)<br>
				f. GAT-B Score card (If applicable)<br>
				g. NET Score card (If applicable)<br>
				h. SLET Score card (If applicable)<br>
				i. Category Certificate (If applicable)<br>
				j. Additional Documents (if applicable)
</p>


<p align="left"><strong>Step E</strong>: Click “Upload Documents (Compulsory)” and upload all necessary documents from Class X onwards as per instruction and submit</p>

<p align="left"><strong>Step F:</strong> Click “Check Eligibility” and enter marks and percentage in the respective fields and check your eligibility </p>

<p align="left"><strong>Step G:</strong> Click “Payment and Final Submission”, pay the prescribed fee for online registration using the online payment gateway.</p>

<p align="left"><strong>Step H:</strong> Take a printout of the information submitted. This printout will be required along with the other original documents at the time of admission.</p>

<p align="left"><strong>Step I:</strong> Please keep the following documents (as mentioned under heading <b>MANDATORY DOCUMENTS)</b> handy for online submission before filling up the application form. Scan and store the following documents as separate files as they will need to be uploaded as required during the ‘Online Application’ process.</p>

